# eval/evaluators.py

"""
3 custom LangSmith evaluators for the Self-Initiated Research Agent.

Layer 1 evaluators (LLM-as-judge):
  - research_quality_evaluator   → final ResearchBrief quality
  - question_quality_evaluator   → clarifying question quality

Layer 2 evaluator (pure heuristic):
  - efficiency_evaluator         → trajectory efficiency, no LLM needed
"""

import re
import json
from typing import Any
from app.schemas.models import ResearchBrief, ClarifyingQuestion, KnowledgeGap
from app.services.llm import get_llm
import logging

logger = logging.getLogger(__name__)


# ── LAYER 1 — EVALUATOR 1: RESEARCH QUALITY ───────────────────────────────────

RESEARCH_QUALITY_PROMPT = """You are an expert research evaluator. 
Score the following ResearchBrief against the original research goal.

RESEARCH GOAL:
{goal}

RESEARCH BRIEF:
Executive Summary: {executive_summary}
Key Findings: {key_findings}
Remaining Gaps: {remaining_gaps}
Next Steps: {next_steps}
Total Papers Found: {total_papers_found}

Score on exactly these 3 dimensions. Be strict.

COVERAGE (0-3):
  3 = Covers all major subtopics of the goal, includes recent papers, ≥2 gaps identified
  2 = Covers most subtopics, minor gaps in coverage
  1 = Misses major subtopics or no recent papers cited
  0 = Off-topic or empty

RELEVANCE (0-3):
  3 = Every finding directly addresses the research goal
  2 = Most findings relevant, 1-2 tangential
  1 = Several findings are off-target
  0 = Does not address the goal

GAP_IDENTIFICATION (0-3):
  3 = Remaining gaps are specific, actionable, and non-obvious
  2 = Gaps identified but vague or partially redundant
  1 = Gaps are obvious or unhelpful
  0 = No gaps identified

Return ONLY valid JSON, no explanation:
{{"coverage": <int>, "relevance": <int>, "gap_identification": <int>}}"""


async def research_quality_evaluator(
    brief: ResearchBrief,
    goal: str,
) -> dict[str, Any]:
    """
    LLM-as-judge pointwise scoring on 3 rubric dimensions.
    Total score: 0-9.

    Returns:
        {
            "score": float (0.0 - 1.0, normalized),
            "raw_score": int (0-9),
            "breakdown": {"coverage": int, "relevance": int, "gap_identification": int}
        }
    """
    llm = get_llm()

    prompt = RESEARCH_QUALITY_PROMPT.format(
        goal=goal,
        executive_summary=brief.executive_summary,
        key_findings="\n".join(f"- {f}" for f in brief.key_findings),
        remaining_gaps="\n".join(f"- {g}" for g in brief.remaining_gaps),
        next_steps="\n".join(f"- {s}" for s in brief.next_steps),
        total_papers_found=brief.total_papers_found,
    )

    try:
        response = await llm.ainvoke(prompt)
        raw = response.content.strip()

        # Strip markdown code fences if present
        raw = re.sub(r"```json|```", "", raw).strip()
        breakdown = json.loads(raw)

        raw_score = (
            breakdown.get("coverage", 0)
            + breakdown.get("relevance", 0)
            + breakdown.get("gap_identification", 0)
        )

        return {
            "score": round(raw_score / 9, 3),   # normalize to 0.0-1.0
            "raw_score": raw_score,
            "breakdown": breakdown,
        }

    except Exception as e:
        logger.error(f"research_quality_evaluator failed: {e}")
        return {"score": 0.0, "raw_score": 0, "breakdown": {}, "error": str(e)}


# ── LAYER 1 — EVALUATOR 2: QUESTION QUALITY ───────────────────────────────────

QUESTION_QUALITY_PROMPT = """You are evaluating clarifying questions generated by a research agent.

KNOWLEDGE GAPS the agent identified:
{gaps}

CLARIFYING QUESTIONS the agent generated:
{questions}

Score on exactly these 3 dimensions:

RELEVANCE_TO_GAP (0-3):
  3 = Every question directly addresses one of the listed gaps
  2 = Most questions map to gaps, 1-2 are tangential
  1 = Questions loosely related to gaps
  0 = Questions ignore the gaps entirely

NON_REDUNDANCY (0-3):
  3 = All questions are distinct, no overlap
  2 = Minor overlap between 1-2 questions
  1 = Several questions ask the same thing differently
  0 = Mostly redundant

ACTIONABILITY (0-3):
  3 = Answers to these questions would concretely change the research direction
  2 = Answers would somewhat refine the research
  1 = Answers would make little difference
  0 = Questions are unanswerable or irrelevant

Return ONLY valid JSON, no explanation:
{{"relevance_to_gap": <int>, "non_redundancy": <int>, "actionability": <int>}}"""


async def question_quality_evaluator(
    questions: list[ClarifyingQuestion],
    gaps: list[KnowledgeGap],
) -> dict[str, Any]:
    """
    LLM-as-judge scoring on clarifying question quality.
    Total score: 0-9.

    Returns:
        {
            "score": float (0.0 - 1.0, normalized),
            "raw_score": int (0-9),
            "breakdown": {"relevance_to_gap": int, "non_redundancy": int, "actionability": int}
        }
    """
    if not questions:
        return {"score": 0.0, "raw_score": 0, "breakdown": {}, "error": "no questions"}

    llm = get_llm()

    formatted_gaps = "\n".join(
        f"- [{g.gap_id}] {g.description} (priority={g.priority})" for g in gaps
    )
    formatted_questions = "\n".join(
        f"- [{q.gap_id}] ({q.question_type}) {q.question}" for q in questions
    )

    prompt = QUESTION_QUALITY_PROMPT.format(
        gaps=formatted_gaps,
        questions=formatted_questions,
    )

    try:
        response = await llm.ainvoke(prompt)
        raw = re.sub(r"```json|```", "", response.content.strip()).strip()
        breakdown = json.loads(raw)

        raw_score = (
            breakdown.get("relevance_to_gap", 0)
            + breakdown.get("non_redundancy", 0)
            + breakdown.get("actionability", 0)
        )

        return {
            "score": round(raw_score / 9, 3),
            "raw_score": raw_score,
            "breakdown": breakdown,
        }

    except Exception as e:
        logger.error(f"question_quality_evaluator failed: {e}")
        return {"score": 0.0, "raw_score": 0, "breakdown": {}, "error": str(e)}


# ── LAYER 2 — EVALUATOR 3: EFFICIENCY (NO LLM) ────────────────────────────────

def efficiency_evaluator(
    iteration_count: int,
    search_queries_tried: list[str],
    failed_searches: list[str],
) -> dict[str, Any]:
    """
    Pure heuristic trajectory efficiency scorer. No LLM needed.

    Scoring:
      1.0  if iterations <= 3
      0.5  if iterations == 4-5
      0.0  if iterations > 5
      -0.2 penalty if any failed query was retried (loop detection)

    Returns:
        {
            "score": float,
            "iterations": int,
            "repeated_failures": list[str],
            "penalty_applied": bool
        }
    """
    # Base score from iteration count
    if iteration_count <= 3:
        base_score = 1.0
    elif iteration_count <= 5:
        base_score = 0.5
    else:
        base_score = 0.0

    # Loop detection — did agent retry a query that already failed?
    # AgentState.search_queries_tried = all queries ever tried
    # AgentState.failed_searches      = queries that returned 0 results
    repeated_failures = list(
        set(search_queries_tried) & set(failed_searches)
    )

    penalty_applied = len(repeated_failures) > 0
    final_score = max(0.0, base_score - (0.2 if penalty_applied else 0.0))

    return {
        "score": round(final_score, 3),
        "iterations": iteration_count,
        "repeated_failures": repeated_failures,
        "penalty_applied": penalty_applied,
    }